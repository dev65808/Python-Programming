# Scenario-1
age = 22
if age >= 18:
    print("Eligible")
else:
    print("Not eligible")

# Scenario-2
age = 22
if age >= 18:
    message = "Eligible"
else:
    message = "Not eligible"
print(message)

# Scenario-3 (Ternary Operator)
age = 22
message = "Eligible" if age >= 18 else "Not eligible"
print(message)
